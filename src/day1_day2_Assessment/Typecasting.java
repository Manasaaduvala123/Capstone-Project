package day1_day2_Assessment;

public class Typecasting {

	public static void main(String args[])
	{
		System.out.println("CONVERTING INTEGER TO DOUBLE DATA TYPE :");
		int i=20;
		double d=(double)i;
		System.out.println("Integer datatype value is :"+i);
		System.out.println("Double Data type value is :"+d);
		System.out.println("CONVERTING DOUBLE TO INTEGER DATA TYPE :");
		double d1=56.72634d;
		int i1=(int)d1;
		System.out.println("Double Data type value is :"+d1);
		System.out.println("Integer datatype value is :"+i1);
	}

}

