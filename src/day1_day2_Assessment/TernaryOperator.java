package day1_day2_Assessment;

public class TernaryOperator {
	    public static void main(String[] args)
	    {
	        int n1 = 5, n2 = 10, max;
	        System.out.println("First num: " + n1);
	        System.out.println("Second num: " + n2);
	        max = (n1 > n2) ? n1 : n2;
	        System.out.println("Maximum is = " + max);
	    }
	}

/*
First num: 5
Second num: 10
Maximum is = 10
*/